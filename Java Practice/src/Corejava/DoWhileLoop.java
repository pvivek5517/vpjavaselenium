package Corejava;

public class DoWhileLoop {
	
	public static void main (String[] args) {
		
		int count=0;
		do {
			count=count+5;
			System.out.println("count is " + count);
		} while (count < 25);
		
		int count1=25;
		do {
			count1=count1+5;
			System.out.println("count1 is " + count1);
		} while (count1 < 25);
	}

}
