package Corejava;

public class WhileLoop {
	
	public static void main (String[]args) {
		
		int Count = 0;
		
		while (Count < 25) {
			Count = Count + 5;
			System.out.println("Count is " + Count);
		}
		int Count1 = 25;
		while (Count1 < 25) {
			Count1 = Count1 + 5;
			System.out.println("Count1 is " + Count1);
		}
	}

}
