package Corejava;

public class ForLoop {
	
	public static void main (String [] args) {
		
		for (int i=0; i<=5; i++) {
			System.out.println("The count is " + i);
		}
		
		for (int j=5; j>=0; j--) {
			System.out.println("The count is " + j);
		}
		for (int k=0; k<=5; k+=2) {
			System.out.println("The count is " + k);
		}
		
		
	}

}
